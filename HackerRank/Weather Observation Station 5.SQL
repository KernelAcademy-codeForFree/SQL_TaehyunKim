SELECT * FROM (SELECT CITY, LENGTH(CITY) FROM STATION ORDER BY LENGTH(CITY) ASC, CITY ASC)
WHERE ROWNUM = 1
-- 길이가 가장 짧은 CITY, LENGTH(CITY) 오름차순 후, CITY 오름차순(알파벳 정렬) 그 후 ROWNUM = 1
UNION
SELECT * FROM (SELECT CITY, LENGTH(CITY) FROM STATION ORDER BY LENGTH(CITY) DESC, LENGTH(CITY) ASC)
WHERE ROWNUM = 1;
-- 길이가 가장 긴 CITY, LENGTH(CITY) 내림차순 후, CITY 오름차순(알파벳 정렬) 그 후 ROWNUM = 1